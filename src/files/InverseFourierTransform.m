function reconstructedImage = InverseFourierTransform(frequencyMatrix)
    % ?????? ?? ?????? Double ???? ????????
    frequencyMatrix = double(frequencyMatrix);
    [rows, cols, channels] = size(frequencyMatrix);
    reconstructedImage = zeros(rows, cols, channels);
    
    for k = 1:channels
        currentFreqChannel = frequencyMatrix(:, :, k);
        
        % 1. ???? ??? Shift (??? ???? ?? ??? ???? fftshift ?? ??? DFT)
        shiftedChannel = ifftshift(currentFreqChannel);
        
        % 2. ??? ??? Inverse Fourier
        spatialData = ifft2(shiftedChannel);
        
        % 3. ??? ????? ???????
        realData = real(spatialData);
        
        % 4. ??? Scaling: ????? ????? ????? [0, 1] ?? ?? [0, 255]
        % ?? ????? ?? ???? ??? ???? ??? ???? ?? uint8
        minVal = min(realData(:));
        maxVal = max(realData(:));
        if maxVal > minVal
            normalizedData = (realData - minVal) / (maxVal - minVal);
        else
            normalizedData = realData;
        end
        
        reconstructedImage(:, :, k) = normalizedData * 255;
    end
    
    % ??????? ??????? ?? uint8 ?????
    reconstructedImage = uint8(reconstructedImage);
end